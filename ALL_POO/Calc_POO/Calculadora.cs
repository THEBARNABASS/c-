namespace Calc_POO
{
    public class Calculadora
    {

        public float n1;
        public float n2;

        public float Somar()
        {
            return this.n1 + this.n2;
        }


        // *********SUBTRAÇÃO***************

        public float Sub()
        {
            return this.n1 - this.n2;
        }

        // *********MULTIPLICAÇÃO***************

        public float Multi()
        {
            return this.n1 * this.n2;
        }

        // *********DIVISÃO***************

        public float Div()
        {
            return this.n1 / this.n2;
        }
    }
}







